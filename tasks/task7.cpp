//                        C
//                      i   I
//                    s       s
//                  b           b
//                e               e
//              s                   s
//            t s e b s i C i s b e s t

#include <iostream>
using namespace std;

int main()
{
    cout << "                        C" << "\n";
    cout << "                      i   I" << "\n";
    cout << "                    s       s" << "\n";
    cout << "                  b           b" << "\n";
    cout << "                e               e" << "\n";
    cout << "              s                   s" << "\n";
    cout << "            t s e b s i C i s b e s t" << "\n";
    return 0;
}