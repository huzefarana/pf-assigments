// Task.5
// Write a C++ program to declare two integers, one float 
// variable and assign 10, 15, and 12.6 to them respectively. Then
// prints these values on the screen.

#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 15;
    float c = 12.6;
    cout << "a = " << a << "\n";
    cout << "b = " << b << "\n";
    cout << "c = " << c << "\n";
    return 0;
}